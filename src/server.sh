#!/bin/bash

x="start"

menu ()
{
while true $x != "start"
do
clear
echo "=============================================================================================="
echo "                                   SERVER"
echo "=============================================================================================="
echo ""
echo "------------------------------------------"
echo "    Config                      - 1"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Users                       - 2"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Groups                      - 3"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    SAMBA                       - 4"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    WEB                         - 5"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Tools                       - 9"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Exit                        - 0"
echo "------------------------------------------"
echo ""
echo "Inform the menu option (number): "
read x
echo ""
echo "Menu option ($x)"
echo "=============================================================================================="
echo ""
echo "=============================================================================================="

case "$x" in


    1)
      echo "Config [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./config.sh
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    2)
      echo "Users [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./users.sh
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    3)
      echo "Groups [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./groups.sh
echo ""
echo 
"=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    4)
      echo "SAMBA [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./samba.sh
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    5)
      echo "WEB [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./web.sh
echo ""
echo 
"=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    9)
      echo "Tools [...]"
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./tools.sh
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    0)
      echo "Exiting..."
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      clear
      exit
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;

*)
        echo "Invalid option!"
esac
done

}
menu
