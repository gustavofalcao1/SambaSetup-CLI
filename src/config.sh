#!/bin/bash

x="start"

menu ()
{
while true $x != "start"
do
clear
echo "=============================================================================================="
echo "                                   CONFIG"
echo "=============================================================================================="
echo ""
echo "------------------------------------------"
echo "    Update Server               - 1"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Reboot Server               - 2"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Return                      - 9"
echo "------------------------------------------"
echo "------------------------------------------"
echo "    Exit                        - 0"
echo "------------------------------------------"
echo ""
echo "Inform the menu option (number): "
read x
echo ""
echo "Menu option ($x)"
echo "=============================================================================================="
echo ""
echo "=============================================================================================="

case "$x" in


    1)
      echo "Update all Server packages..."
      echo ""
      sudo apt update
      sudo apt upgrade -y
      sudo apt dist-upgrade -y
      sudo apt autoremove -y
      sudo apt clean
      echo ""
      echo "SUCCESS!"
      sleep 2
echo ""
echo 
"=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    2)
      echo "Rebooting..."
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      sudo reboot
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    9)
      echo "Returning..."
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      ./server.sh
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;
    0)
      echo "Exiting..."
      echo ""
      echo "SUCCESS!"
      echo ""
      sleep 2
      clear
      exit
echo ""
echo "=============================================================================================="
echo ""
echo "=============================================================================================="
;;

*)
        echo "Invalid option!"
esac
done

}
menu
